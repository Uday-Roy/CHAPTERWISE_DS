# 🎮 THE TRADITIONAL "HELLO WORLD" - Every programmer's first step!
print("Hello, World!")  # The most famous line in programming history

# 🚀 MULTIPLE WAYS TO SAY HELLO
print("Hello, Python Master!")  # You're now a Python programmer!
print("Welcome to the Python Adventure!")  # The journey begins here

# 🎨 PRINT WITH STYLE - Multiple arguments
print("Python", "is", "awesome!", "Let's", "learn", "together!")  
# Output: Python is awesome! Let's learn together!

# 🔢 PRINTING NUMBERS
print(42)  # The answer to life, universe, and everything
print(3.14159)  # Pi - a mathematical constant

# ✨ COMBINING TEXT AND NUMBERS
print("The answer is:", 42)  # Automatic space between arguments

# 📝 ESCAPE CHARACTERS - Make your text fancy!
print("Hello\nWorld!")  # \n creates a new line
# Output:
# Hello
# World!

print("Hello\tWorld!")  # \t creates a tab space
# Output: Hello   World!

print("She said: \"Python is amazing!\"")  # \" to print quotes
# Output: She said: "Python is amazing!"

print("C:\\Users\\YourName")  # \\ to print backslash
# Output: C:\Users\YourName

# 🎯 MULTI-LINE STRINGS - Triple quotes for long text
print("""This is a
multi-line
string!""")
# Output:
# This is a
# multi-line
# string!

# 💡 FUN FACT: Python is named after Monty Python, not the snake!
# Let's celebrate with a funny print:
print("🐍 Python - More powerful than a snake, more fun than Monty!")

# 🧪 CHALLENGE YOURSELF:
# Can you print your name, age, and favorite hobby in one line?
print("Name: Alice", "Age: 25", "Hobby: Coding", sep=" | ")
# Output: Name: Alice | Age: 25 | Hobby: Coding

# 🌟 SEPARATOR AND END PARAMETERS - Customize your print!
print("Apple", "Banana", "Orange", sep=", ", end=" 🍍\n")
# Output: Apple, Banana, Orange 🍍

# 📚 WHAT WE LEARNED:
# 1. print() is Python's output function
# 2. Strings are written in quotes
# 3. Comments start with # 
# 4. Python executes code line by line (interpreted)
# 5. Escape sequences like \n, \t, \", \\
# 6. sep and end parameters for customization

